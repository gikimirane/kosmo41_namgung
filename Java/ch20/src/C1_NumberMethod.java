
public class C1_NumberMethod {

	public static void main(String[] args) {
		Integer num1 = new Integer(29);
		System.out.println(num1.intValue());   //int num1 을 int형으로 보여줘
		System.out.println(num1.doubleValue()); //int num1을 double형으로 보여줘
		
		Double num2 = new Double(3.14);
		System.out.println(num2.intValue()); //double num2를 int형으로 보여줘
		System.out.println(num2.doubleValue()); //double num2를 double형으로 보여줘
	}

}
